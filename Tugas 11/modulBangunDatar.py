# Luas persegi
def luas_persegi(s):
    return s*s

# Luas persegi panjang
def luas_persegipanjang(p,l):
    return p*l

# Luas Segitiga
def luas_segitiga(a,t):
    return 0.5*a*t 

# Luas lingkaran
def luas_lingkaran(phi,r):
    return phi*r*r

#layang layang
def luas_layanglayang(d1,d2):
    return (d1*d2)/2