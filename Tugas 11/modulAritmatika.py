def tambah():
    b1 = int(input("bilangan pertama >>"))
    b2 = int(input("bilangan kedua >>"))

    hasil = b1 + b2
    print("hasil tambah dari", b1, "+", b2, "=", hasil)

def kurang():
    b1 = int(input("bilangan pertama >>"))
    b2 = int(input("bilangan kedua >>"))

    hasil = b1 - b2
    print("hasil kurang dari", b1, "-", b2, "=", hasil)

def kali():
    b1 = int(input("bilangan pertama >>"))
    b2 = int(input("bilangan kedua >>"))

    hasil = b1 * b2
    print("hasil kali dari", b1, "*", b2, "=", hasil)